public class seatfirst {


    String a,d,g,k;

    public seatfirst(String a, String d, String g, String k) {
        this.a = a;
        this.d = d;
        this.g = g;
        this.k = k;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getD() {
        return d;
    }

    public void setD(String d) {
        this.d = d;
    }

    public String getG() {
        return g;
    }

    public void setG(String g) {
        this.g = g;
    }

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }
}
